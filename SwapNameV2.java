
public class SwapNameV2 {

	public static void main(String[] args) {
String name1="pranay";
String name2="ruthik";
System.out.println("Before Swapping-------");
System.out.println("Name1="+name1);
System.out.println("Name2="+name2);
String t=name2;
name2=name1;
name1=t;
System.out.println("After swapping........");
System.out.println("Name1="+name1);
System.out.println("Name2="+name2);
	}

}
